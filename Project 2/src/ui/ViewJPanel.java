    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int sri = tblvital.getSelectedRow();
       if (sri < 0)
       {
           JOptionPane.showMessageDialog(this, "Please Select a row to View");
           return;
       }
       DefaultTableModel model = (DefaultTableModel) tblvital.getModel();
       Vitals sv = (Vitals) model.getValueAt(sri, 0);
        
       temptxt.setText(String.valueOf(sv.getTemprature()));
       bptxt.setText(String.valueOf(sv.getBloodpressure()));
       pulsetxt.setText(String.valueOf(sv.getPluse()));
       datetxt.setText(sv.getDate());
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        
       int sri = tblvital.getSelectedRow();
       if (sri < 0)
       {
           JOptionPane.showMessageDialog(this, "Please Select a row to Delete");
           return;
       }
       DefaultTableModel model = (DefaultTableModel) tblvital.getModel();
       Vitals sv = (Vitals) model.getValueAt(sri, 0);
       
       vitalhistory.deleteVitals(sv);
       
       JOptionPane.showMessageDialog(this , "Vitals Has been deleted");
       
       populateTabel();
    }//GEN-LAST:event_deletebtnActionPerformed


