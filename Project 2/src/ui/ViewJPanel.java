    private void populateTabel() 
    {
    
        DefaultTableModel model = (DefaultTableModel) tblvital.getModel();
        model.setRowCount(0);
        
        for (Vitals vs : vitalhistory.getHistory())
        {
            Object[] row = new Object[3];
            row[0] = vs;
            row[1] = vs.getTemprature();
            row[2] = vs.getPluse();
            
            model.addRow(row);
        }
    }
