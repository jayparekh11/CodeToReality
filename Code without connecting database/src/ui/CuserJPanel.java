    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       //this.person = person;
       if(!(nuidtxt.getText().chars().allMatch(Character::isDigit))){
                JOptionPane.showMessageDialog(null, "NU ID should only be digits!");
                return;
            }
            if(nuidtxt.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please enter NU ID");
                return;
            }
            if(unametxt.getText().equals("")){
                 JOptionPane.showMessageDialog(null, "Please enter Username");
                 return;
             } 
            if((passtxt.getText().equals(""))){
                 JOptionPane.showMessageDialog(null, "Passwords Cannot be null!");
                 return; }

        Person person=(Person)tblvital1.getValueAt(tblvital1.getSelectedRow(), 0);

            for(Person p :personhistory.getPersonlist()){
                if(p.getNuid()==person.getNuid())
                {
                //  person.setUserID( txtUserID.getText());
                // person.setNUID(txtUserNUID.getText());
                 // person.setUsername(txtUserName.getText());
                    person.setNuid(nuidtxt.getText());
                    person.setUsername(unametxt.getText());
                    person.setPassword(passtxt.getText());
                    person.getPasswordlist().add(this.encrypt.encryptPassword(passtxt.getText()));
                    //person.setEnable(true);//To convert person to user
                    System.out.println("Encrypted Password : "+person.getPasswordlist());
                    if (yesbtn.isSelected())
                    {
                        person.setEnable(true);
                    }
                     else
                    {
                        person.setEnable(false);
                    }
                }
            }
       
       JOptionPane.showMessageDialog(this, "User has been created");
       
       

              
              uidtxt.setText("");
              nuidtxt.setText("");
              unametxt.setText("");
              passtxt.setText("");
              roletxt.setText("");
              yesbtn.setSelected(false);
              nobtn.setSelected(false);
             
              populateTabel();

              
        
    }//GEN-LAST:event_jButton1ActionPerformed

