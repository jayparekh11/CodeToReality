    private void populateTabel() 
    {
    
        DefaultTableModel model = (DefaultTableModel) tblsearch.getModel();
        model.setRowCount(0);
        
        for (Person ps : personhistory.getPersonlist())
        {
            Object[] row = new Object[7];
            //row[0] = ps;
//            row[0] = ps.getNuid();
//            row[1] = ps.getUsername();
//            row[2] = ps.getPassword();
//            row[3] = ps.getRole();  
            
            row[0] = ps;
            row[1] = ps.getName();
            row[2] = ps.getEmail();
            row[3] = ps.getUsername();
            row[4] = ps.getPassword();
            row[5] = ps.getRole();
            row[6] = ps.getEnable();
            model.addRow(row);
        }
    }
